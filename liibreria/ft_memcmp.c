int	ft_memcmp(const void *ptr1, const void *ptr2, size_t num)
{
	int	i;

	i = 0;
	while (ptr1[i] == ptr2[i] && i < num)
		i++;
	if (ptr1[i] != ptr2[i])
		return (ptr1[i] - ptr2[i]);
	return (0);
}
