void	*ft_memchr(const void *ptr, int value, size_t num)
{
	int	i;

	i = 0;
	while (ptr[i] != num)
	{
		if (ptr[i] == value)
			return ((char *) &ptr[i]);
		i++;
	}
	return (0);
}
